var1 = int(input('Digame el primer numero, por favor: '))
var2 = int(input('Digame el segundo numero, por favor: '))

print(f'El valor de la suma de los numeros que me indicó es: {var1 + var2}')
print(f'El valor de la resta de los numeros que me indicó es: {var1 - var2}')
print(f'El valor de la división de los numeros que me indicó es: {var1 / var2}')
print(f'El valor de la multiplicación de los numeros que me indicó es: {var1 * var2}')